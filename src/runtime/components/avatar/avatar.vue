<script lang="ts">
import type { BoxProps } from '@nui/components'
import type {
	AnyString,
	Classes,
	NuanceColor,
	NuanceGradient,
	NuanceRadius,
	NuanceSize,
} from '@nui/types'


type AvatarVariant = 'filled' | 'light' | 'gradient' | 'outline' | 'default'

type AvatarClasses = 'root' | 'placeholder' | 'image'

interface AvatarVars {
	root:
		| '--avatar-size'
		| '--avatar-radius'
		| '--avatar-bg'
		| '--avatar-color'
		| '--avatar-bd'
}

export interface AvatarProps extends BoxProps {
	/** Border radius */
	radius?: NuanceRadius | AnyString

	/** Component size */
	size?: NuanceSize | AnyString

	/** Color from theme. Pass `'initials'` to derive the color from the user's name. */
	color?: NuanceColor | 'initials'

	/** Gradient configuration (used with `variant="gradient"`) */
	gradient?: NuanceGradient

	/** Image URL. When the image fails to load or is `null`, a placeholder is shown instead. */
	src?: string | null

	/** Image `alt` attribute. Also used as `title` for the placeholder. */
	alt?: string

	/** Name of the user. Used to render initials and to generate a color when `color="initials"`. */
	name?: string

	/** Palette of colors picked from for autogenerated initials. Defaults to all theme colors except gray and dark. */
	allowedInitialsColors?: NuanceColor[]

	/** Icon rendered when no `src` and no `name` are provided. Defaults to the configured `person` icon. */
	placeholder?: string

	/** Visual variant */
	variant?: AvatarVariant

	/** Styles API */
	classes?: Classes<AvatarClasses>
}
</script>

<script setup lang='ts'>
import { useConfig, useVarsResolver } from '@nui/composables'
import { getRadius, getSize } from '@nui/utils'
import { computed } from 'vue'

import Box from '../box/box.vue'
import css from './avatar.module.css'
import { useAvatarGroupState } from './lib/context'
import { getInitials } from './lib/get-initials'
import { getInitialsColor } from './lib/get-initials-color'


const {
	name,
	radius,
	placeholder,
	color,
	variant,
	allowedInitialsColors,
	gradient,
	size,
	classes,
	mod,
	src,
	...rest
} = defineProps<AvatarProps>()

const initials = computed(() => name && getInitials(name))
const ctx = useAvatarGroupState()

const { icons, variantResolver } = useConfig()
const resolvedPlaceholder = computed(() => placeholder ?? icons.person)

const style = useVarsResolver<AvatarVars>(theme => {
	const _color = color === 'initials' && typeof name === 'string'
		? getInitialsColor(name, allowedInitialsColors)
		: color

	const { background, text, border } = variantResolver({
		theme,
		gradient,
		color: _color || 'gray',
		variant: variant || 'light',
	})

	return {
		root: {
			'--avatar-size': getSize(size, 'avatar-size'),
			'--avatar-radius': radius === undefined ? undefined : getRadius(radius),
			'--avatar-bg': color || variant ? background : undefined,
			'--avatar-color': color || variant ? text : undefined,
			'--avatar-bd': color || variant ? border : undefined,
		},
	}
})
</script>

<template>
	<Box
		:style='style.root'
		:class='[css.root, classes?.root]'
		:mod='[{ "within-group": ctx?.withinGroup }, mod]'
		v-bind='rest'
	>
		<span
			v-if='!src'
			:class='[css.placeholder, classes?.placeholder]'
			:title='alt'
		>
			<slot>
				<Icon v-if='!src && !name' :name='resolvedPlaceholder' />
				{{ initials }}
			</slot>
		</span>
		<NuxtImg
			v-else
			:src
			:alt
			:class='[css.image, classes?.image]'
		/>
	</Box>
</template>
